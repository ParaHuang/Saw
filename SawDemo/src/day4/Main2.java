package day4;

public class Main2 {
    public static void main(String[] args) {
        //operators
        int a = 12;
        int b = 5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);    //12/5 = 2.4 -> 2
        System.out.println(a%b);    //modulo, get remainder

        System.out.println(a<=15);
        System.out.println(a!=b);

        System.out.println(15<=7+8); //true
    }
}
